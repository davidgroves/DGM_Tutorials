def baby_name(animal):
    if animal == "Dog":
        return "Pup"
    elif animal == "Cat":
        return "Kitten"
    elif animal == "Bird":
        return "Chick"
    elif animal == "Kangaroo":
        return "Joey"
    elif animal == "Cow":
        return "Calf"
    else:
        return "Don't know :("

print(baby_name("Dog"))
print(baby_name("Cat"))
print(baby_name("Bird"))
print(baby_name("Kangaroo"))
print(baby_name("Cow"))
print(baby_name("Llama"))
